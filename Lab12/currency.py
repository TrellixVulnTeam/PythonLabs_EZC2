def to_euro(dollar):
    dollar_to_euro_rate = 0.81
    euro = dollar * dollar_to_euro_rate
    return euro

def to_yen(dollar):
    dollar_to_yen_rate = 106.45
    yen = dollar * dollar_to_yen_rate
    return yen

def to_peso(dollar):
    dollar_to_peso_rate = 18.58
    peso = dollar * dollar_to_peso_rate
    return peso
